function [OutS]=gen_data_JR(x)
A=x(1);
B=x(2);
a=100;
b=50;
C1=x(3);
v0=x(4);
C2=0.8* C1;
C3=0.25* C1;
C4=0.25* C1;
Fs = 128;
Ti = 0;
Tf = 7;
SampleNumber = (Tf-Ti)*Fs;
Tspan = linspace(Ti,Tf,SampleNumber);   % Time duration for the data
yi = zeros(6,1);
options = odeset('RelTol',1E-3);
tiw =normrnd(200,30,SampleNumber+1,1);
[~,y] = ode45(@(t,y) odefcn_JansenRit(t,y,tiw,A,B,a,b,C1,C2,C3,C4,Fs,v0), Tspan, yi, options);
rt=2;
data = y(rt*Fs+1:end,2) - y(rt*Fs+1:end,3);
OutS = normrnd(0,(0.2*std(data))^2,5*Fs,1)+data;
